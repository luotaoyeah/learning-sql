-- 2.6. all-at-once operations

USE tsqlv6;

-- region
-- ----------------------------------------------------------------------------------------------------
-- 同一个子句中的多个表达式, 它们的执行没有明确的先后顺序, 比如 SELECT 中的多个列, 比如 WHERE 中的多个表达式,
-- 因此造成的影响是: SELECT 中的多个列之间不能相互引用, WHERE 中不会发生短路效果,

SELECT 1 AS x,
       2 AS y
 WHERE (1 > 0)
    OR (1 < 0);
-- endregion