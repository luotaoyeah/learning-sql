-- 12.6.1 the EXEC command

-- ----------------------------------------------------------------------------------------------------
-- 将 SQL 语句存入字符串变量, 然后使用 EXEC 命令执行这个字符串, 称之为动态 SQL 执行,

DECLARE @sql AS NVARCHAR(MAX);

-- 字符串中的所有 SQL 语句会作为一个批次进行执行,
SET @sql = 'PRINT ''A''; PRINT ''B''; SELECT 1 AS ''C''';

-- 注意要将变量名用圆括号包裹起来, 否则 EXEC 会认为在执行一个 SP,
EXEC (@sql);

GO
